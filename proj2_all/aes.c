/*
 * Generate an AES key schedule
 */
void KeyExpansion(const uint8_t *key, uint32_t *roundKey) // key[16]
{ 

  uint32_t z, x, y, *wkey;
  uint8_t sub_index;
  int index;

  wkey = (uint32_t *)key; // key 32비트로 변환

  // w[0], w[1], w[2], w[3]은 key 변형 없이 사용
  roundKey[0] = wkey[0]; 
  roundKey[1] = wkey[1];
  roundKey[2] = wkey[2];
  roundKey[3] = wkey[3];

  // w[4] ~ w[43] 생성
  for(int i = 0; i < 10; i++) // 총 10 round
  {
    index = 3 + i * 4;
    x = 0;
    y = 0;
    z = 0;

    // 1. g_RotWord
    x = (roundKey[index] << 24 ^ roundKey[index] >> 8);

    // 2. g_SubWord
    for(int j = 0; j < 4; j++)
    {
        sub_index = (uint8_t)(x >> (24 - j*8));
        y = y ^ ((uint32_t)sbox[sub_index] << (24 - j*8));
    }

    // 3. g_Rcon
    z = y ^ Rcon[i+1];

    // 4. roundKey 생성
    roundKey[index+1] = roundKey[index-3] ^ z;
    roundKey[index+2] = roundKey[index+1] ^ roundKey[index-2];
    roundKey[index+3] = roundKey[index+2] ^ roundKey[index-1];
    roundKey[index+4] = roundKey[index+3] ^ roundKey[index];

  }

}

/* AddRoundKey 함수
 * 라운드 키를 XOR 연산을 사용하여 state에 더한다.
 */
static void AddRoundKey(uint8_t *state, const uint32_t *roundKey) 
{
   	uint32_t *temp;
    temp = (uint32_t *)state;

    for(int i = 0; i < 4; i++)	
    {
        temp[i] = temp[i] ^ roundKey[i];
    } 
    memcpy(state, temp, sizeof(uint8_t)*16);

}

/* SubBytes 함수
 * mode에 따라 순방향 또는역방향으로 바이트를 치환한다.
 */
static void SubBytes(uint8_t *state, int mode)
{
    if(mode == 1) 
    {
        for(int i = 0; i < 16; i++)
        {
            state[i] = sbox[state[i]];
        }
    }
    else
    {
        for(int i = 0; i < 16; i++)
        {
            state[i] = isbox[state[i]];
        }
    }
}

/* ShiftRows 함수
 * mode에 따라 순방향 또는 역방향으로 바이트의 위치를 변경한다.
 */
static void ShiftRows(uint8_t *state, int mode)
{
    uint8_t temp[16];
    int index;
    memcpy(temp, state, sizeof(uint8_t)*16);

        for(int i = 1; i < 4; i++) // 0 4 8 12 제외
        {
            for(int j = 0; j < 4; j++) 
            {
                index = 4*j+i;

                if (mode == 1) // 암호화
                {
                    state[index] = temp[(index + 4*i)%16]; // 16 이상은 나머지 값으로 대체

                    // 1 5 9 13   ->  1+4 5+4 9+4 13+4       -> 5 9 13 1
                    // 2 6 10 14  ->  2+8 6+8 10+8 14+8      -> 10 14 2 6
                    // 3 7 11 15  ->  3+12 7+12 11+12 15+12  -> 15 3 7 11
                }

                else // 복호화
                {
                    state[index] = temp[(index + 4*(4-i))%16]; // 16 이상은 나머지 값으로 대체

                    // 1 5 9 13   ->  1+12 5+12 9+12 13+12  -> 13 1 5 9
                    // 2 6 10 14  ->  2+8 6+8 10+8 14+8     -> 10 14 2 6
                    // 3 7 11 15  ->  3+4 7+4 11+4 15+4     -> 7 11 15 3                     
                }
            }
        }
}

/* MixColumns 함수
 * 기약다항식𝑥8 + 𝑥4 + 𝑥3 + 𝑥+ 1을 사용한 GF(28)에서 행렬 곱셈을 수행한다.
 * mode가 DECRYPT이면 역행렬을 곱한다.
 */

uint8_t xtime(uint8_t x)
{
    return ((x << 1) ^ ((x >> 7) & 1 ? 0x1B : 0));
}

uint8_t gf8_mul(uint8_t a, uint8_t b)
{
    uint8_t r = 0;

    while (b > 0) {
        if (b & 1) r = r ^ a; // b가 1이면 r = r XOR a
        b = b >> 1; // b를 한 비트씩 shift
        a = xtime(a); // xtime 함수 호출
    }
    return r;

}

static void MixColumns(uint8_t *state, int mode)
{
    uint8_t temp[16];
    int index, m_index, t_index;
    memcpy(temp, state, sizeof(uint8_t)*16);

    for(int i = 0; i < 4; i++)
    {
        t_index = 4*i;
        for(int j = 0; j < 4; j++)
        {
            index = 4*i+j;
            m_index = 4*j;
            if(mode == 1) // 암호화
            {
                state[index] = gf8_mul(M[m_index],temp[t_index]) ^ gf8_mul(M[m_index+1],temp[t_index+1]) ^
                               gf8_mul(M[m_index+2],temp[t_index+2]) ^ gf8_mul(M[m_index+3],temp[t_index+3]);
            }
            else // 복호화
            {
                state[index] = gf8_mul(IM[m_index],temp[t_index]) ^ gf8_mul(IM[m_index+1],temp[t_index+1]) ^
                               gf8_mul(IM[m_index+2],temp[t_index+2]) ^ gf8_mul(IM[m_index+3],temp[t_index+3]);              
            }
            
        }
    }
}


/*
 * AES cipher function
 * If mode is nonzero, then do encryption, otherwise do decryption.
 */

void static make_roundkey(int index, uint32_t *n_roundKey, const uint32_t *roundKey)
{
    for(int j = 0; j < 4; j++) 
    {
        n_roundKey[j] = roundKey[4*index+j]; // n round key
    }
}

void Cipher(uint8_t *state, const uint32_t *roundKey, int mode)
{
	uint32_t n_roundKey[4];
    if (mode == 1) // 암호화
    {
        // round 1
        AddRoundKey(state,roundKey); 
        for(int i = 1; i < 10; i++)
        {
            SubBytes(state, mode);
            ShiftRows(state, mode);
            MixColumns(state, mode);
            // new roundkey
			      make_roundkey(i, n_roundKey, roundKey);
            AddRoundKey(state, n_roundKey);
        }
        // round 10
        SubBytes(state, mode);
	      ShiftRows(state, mode);
        // new roundkey
		    make_roundkey(10, n_roundKey, roundKey);
        AddRoundKey(state, n_roundKey);
    }
    else // 복호화
    {
        // round 1
		    make_roundkey(10, n_roundKey, roundKey);
        AddRoundKey(state, n_roundKey); 
        for(int i = 1; i < 10; i++)
        {
            SubBytes(state, mode);
            ShiftRows(state, mode);
            MixColumns(state, mode);
            // new roundkey
			      make_roundkey(10-i, n_roundKey, roundKey);
            MixColumns((uint8_t *)n_roundKey, mode);
            AddRoundKey(state, n_roundKey);
        }
        // round 10
        SubBytes(state, mode);
	      ShiftRows(state, mode);
        AddRoundKey(state,roundKey);
    }

}
